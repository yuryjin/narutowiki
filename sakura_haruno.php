        <h3>Childhood</h3>
        <p>
          Sakura is the only child of Mebuki and Kizashi Haruno. She had an
          ordinary childhood, raised by her parents without any serious tragedy
          or complication. When she entered Konoha's Academy, some of the girls
          in her class started picking on her because of her broad forehead.
          Sakura tried to combat their teasing by hiding her forehead with her
          bangs, but this proved to the other girls that it bothered her and
          caused them to tease her even more. Ino Yamanaka, one of her
          classmates, saw this, defended Sakura from her bullies, and encouraged
          her to embrace her forehead rather than hide it.
        </p>
        <p>
          Over the following years, Ino's guidance and friendship helped Sakura
          become comfortable with herself and develop into her own person.
          Though she felt indebted to Ino for helping her, Sakura began to feel
          that she was living in Ino's shadow when she, instead, wanted to be
          Ino's equal. At some point after she entered in the Academy, Sakura
          met Sasuke Uchiha and she developed a crush on him. When she told her
          friends, she was surprised to learn how popular Sasuke was with the
          girls. Sakura heard a rumour of Sasuke being attracted to girls with
          long hair and she began letting her hair grow to get his attention. A
          couple of years later, when she learned Ino had a crush on Sasuke,
          Sakura ended their friendship so that they could compete for Sasuke's
          love, thus beginning a bitter rivalry between them. In the anime,
          Sakura approached Ino, telling her of her liking towards Sasuke.
          Sakura took it to end their friendship, starting their dislike towards
          each other in the beginning of the series.
