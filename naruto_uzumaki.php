        <h3>Childhood</h3>
        <p>
          Naruto was born on the night of October 10th to Minato Namikaze (the
          Fourth Hokage) and Kushina Uzumaki (the second jinchūriki of the
          Nine-Tails). He was named after Naruto Musasabi, the protagonist of
          Jiraiya's first book, which made the Sannin his godfather. The Third
          Hokage made special arrangements for Minato to preserve Kushina's seal
          containing the Nine-Tails while she gave birth to Naruto in a remote
          location, escorted by midwives and Anbu. A masked man, Tobi, tracked
          down their location, however, killed the midwives and Anbu, and
          captured Naruto, forcing Minato to quickly rescue him and teleport him
          to a safe house.
        </p>
        <p>
          With Minato gone, Tobi captured Kushina and released the Nine-Tails
          from her, using it to devastate Konoha. Minato saved Kushina and left
          Naruto in her care before he went to protect the village, eventually
          defeating Tobi and freeing the Nine-Tails from Tobi's control.
          Returning to Naruto and Kushina's location, Minato realised the only
          way to stop the Nine-Tails was to seal it within Naruto, believing
          that his son would someday need the fox's power to defeat Tobi when he
          returned. Since the Nine-Tails' chakra was too immense to seal into an
          infant, Minato sacrificed his soul to split the fox's chakra in half,
          sealing the Yin half within himself and the Yang half within Naruto.
          After telling Naruto how much they loved him, Minato and Kushina
          succumbed to their wounds from protecting their son from the
          Nine-Tails and passed away.
        </p>
        <p>
          Orphaned, not having parents or anyone else to provide for him, Naruto
          received monthly income from the village in order to afford daily
          necessities. Naruto grew up not knowing who his parents were,
          receiving only his mother's surname, as Hiruzen wanted to protect
          Naruto from his father's enemies. Minato's dying wish that Naruto be
          regarded as a hero was honoured by the very few who could put aside
          their pain and losses caused by the disaster, while the majority of
          Konoha, however having no knowledge of the circumstances surrounding
          his birth, openly ostracised and resented Naruto for containing the
          beast that devastated the village and took many lives; some even
          viewed Naruto as the Nine-Tails itself. Soon, the Third Hokage forbade
          anyone from mentioning the Nine-Tails, hoping that the younger
          generation would not blindly hate Naruto as their parents did.
          However, Naruto's peers emulated their parents' hatred of him, despite
          not knowing why. This social isolation caused Naruto to crave
          acknowledgement, which he would gain by pulling pranks.
        </p>
