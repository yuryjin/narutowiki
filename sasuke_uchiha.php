        <h3>Childhood</h3>
        <p>
          Sasuke is the second and youngest son of Mikoto and Fugaku Uchiha;
          they named him after Sasuke Sarutobi in the hopes that he would
          someday be just as strong of a shinobi. Sasuke grew up in the shadow
          of his older brother, Itachi, a natural prodigy who many in the Uchiha
          clan and the village would constantly compare Sasuke and any of his
          accomplishments to. Sasuke himself adored Itachi, never passing up an
          opportunity to spend time with him. Although Itachi welcomed his
          company, letting Sasuke watch him train and taking him on adventures
          into the forests, Itachi in return rarely helped Sasuke himself become
          a better shinobi; when asked, he would often instead poke Sasuke's
          forehead and promise to do so some other time. Sasuke found this
          annoying, but didn't allow it to blemish his high opinion of his
          brother.
        </p>
        <p>
          On entering the Konoha Ninja Academy, Sasuke proved to be the standout
          of his class, consistently getting top grades. However, he could never
          meet the same milestones Itachi had set, resulting in their father
          paying Sasuke little attention. Aware of this neglect, Itachi, despite
          being increasingly busy, tried to stand in for their father by giving
          Sasuke the recognition he craved, at times even blackmailing Fugaku to
          spend time with Sasuke. As time went on, Itachi started becoming
          distant with and cold towards their family, culminating in a falling
          out with much of the Uchiha clan on their suspicion that he'd killed
          his best friend, Shisui Uchiha. Sasuke did not understand the reason
          for this, but he didn't mind the side-effect: his father began taking
          an interest in his development. Fugaku taught Sasuke how to perform
          the Great Fireball Technique, which he mastered in a week. Fugaku
          stated his pride in Sasuke for this accomplishment, but at the same
          time encouraged him not to follow in Itachi's footsteps.
