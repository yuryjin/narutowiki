        <h3>Background</h3>
        <p>
          Itachi was the first child born to Mikoto and Fugaku Uchiha. His early
          childhood was marked with violence: when he was four years old, the
          Third Shinobi World War waged and he witnessed first-hand many of the
          war's casualties. The death and destruction he experienced at such a
          young age traumatised Itachi and made him a pacifist, leading to him
          training non-stop to achieve his dream of becoming a ninja among ninja
          in order to erase fighting from the world. At age 5, after becoming a
          big brother to Sasuke, Itachi was approached by Shisui, who offered to
          train together. Soon, the two became best friends, forming a
          brother-like bond as they continued to teach the other new tricks.
          During the Nine-Tailed Demon Fox's Attack, he was home alone looking
          after Sasuke, followed by Itachi rescuing Mikoto from falling debris,
          and in the anime seeking shelter with Izumi. The Nine-Tails' attack
          had soured relations between Konoha's leadership and the Uchiha, the
          former believing the latter to be responsible. The Uchiha were
          relocated to the edge of the village, isolating them from the rest of
          the village and making it easier to monitor them.
        </p>
        <p>
          At age six, Itachi enrolled into the Academy, where he consistently
          scored the highest in each subject and quickly learned any skill
          taught to him, resulting in him being praised as the best of his
          generation. After four months, his teachers unanimously agreed let him
          take the graduation exam early due to Itachi being more than advanced
          enough for genin-level, which he passed later that month. During this
          time, he began seriously studying the history of the world, gaining a
          great appreciation for it. On the day he graduated as the youngest
          post-war graduate, he was approached by Danzō Shimura, who assessed
          Itachi's thinking.
